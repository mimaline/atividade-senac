function loadUsersToTable(aListaUsuarios, status_filtro, adicionaDados, idLista) {
    if (idLista == undefined) {
        idLista = false;
    }

    let aListaDados = new Array();
    const aListaDesistentes = getListaDesistentes();

    // Pega o codigo da Lista de Exercicios Atual
    let codigoLista = 0;
    aListaUsuarios.forEach(function(value, key) {
        if (key == 0) {
            codigoLista = value[1];
        }
    });

    aListaUsuarios.forEach(function(value, key) {
        const codigo = value[0];
        const user = value[1];
        const status = value[2];
        const nome = value[3];
        const data = value[4];
        const observacao = value[5];

        let temDados = false;
        // Se tiver a lista, verifica se e a lista a ser listada e a mesma da iteracao atual
        if (idLista && parseInt(codigoLista) > 0) {
            if (codigoLista == idLista) {
                temDados = true;
            }
        } else {
            if ((status_filtro == "TODOS") || (status_filtro == status)) {
                temDados = true;
            }
        }

        let alunoDesistente = false;
        if (aListaDesistentes.includes(codigo)) {
            alunoDesistente = true;
        }

        if (temDados && !alunoDesistente) {
            aListaDados.push(data);

            // Carrega os usuarios para a tabela html
            if (adicionaDados) {
                loadDataFromHTML(user, codigo, status, data, observacao, nome);
            }
        }
    });

    return aListaDados;
}

